public class Mem2 {
	private String name;
	private String sname;
	private int age;
	public Mem2 () {};

	public Mem2 (String name, String sname, int age) {
		this.name = name;
		this.sname = sname;
		this.age = age;
	}

	public String getName() {
		return this.name;
	}

	public String getSname() {
		return this.sname;
	}

	public int getAge() {
		return this.age;
	}
} // END class
